package gg.oddysian.death.deathskits.objects;

public class Item {

    private String name;
    private int amount;
    private String tag;

    public String getName(){
        return name;
    }

    public void setName(String name){
        this.name = name;
    }

    public int getAmount(){
        return amount;
    }

    public void setAmount(int amount){
        this.amount = amount;
    }

    public String getTag(){
        return name;
    }

    public void setTag(String tag){
        this.name = tag;
    }

}
